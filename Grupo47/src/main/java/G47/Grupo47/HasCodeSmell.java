package G47.Grupo47;

public class HasCodeSmell {
	private String name;
	private String hasCodeSmell;

	public HasCodeSmell(String name, String hasCodeSmell) {
		this.name = name;
		this.hasCodeSmell = hasCodeSmell;

	}

	public String getName() {
		return name;
	}

	public String getHasCodeSmell() {
		return hasCodeSmell;
	}

}
